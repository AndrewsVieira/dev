package src.model.utils;

public class Provider {
    private String name;

    public Provider(){}

    public Provider(String name) {
        setName(name);
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
